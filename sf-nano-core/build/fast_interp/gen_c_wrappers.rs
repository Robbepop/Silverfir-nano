// C wrapper generation for fast interpreter
// Generates wrapper functions for vm_trampoline.c
//
// Dispatch patterns:
// - Guard-check (default): Preloads nh for next handler. Checks if np == pc_next(pc).
//   Linear path dispatches to preloaded nh; trap/branch path reloads from np->handler.
//   The compiler eliminates the guard for always-linear handlers (inlined impl returns pc_next).
// - Nonlinear: Always reloads np->handler. Used for br, br_table, else_, call_local, return, term, data.

use std::fs;
use std::path::PathBuf;

use super::tos_config::tos_variant_names;
use super::types::{DispatchMode, HandlersFile, HandlerVariantSource, TosPattern, TosPatternString};

/// Check if a handler uses nonlinear dispatch (always reloads from np->handler).
fn is_nonlinear(dispatch: &Option<DispatchMode>) -> bool {
    matches!(dispatch, Some(DispatchMode::Nonlinear))
}

/// Generate the dispatch tail for guard-check pattern (default).
/// Used for always-linear, trapping, and conditional-branch handlers.
///
/// On non-Windows targets, uses a guard: if np == pc_next(pc), dispatch to
/// preloaded nh (zero-latency); otherwise reload from np->handler.
/// The compiler eliminates the guard for always-linear handlers.
///
/// On Windows x86-64, the guard adds overhead because the CPU's indirect branch
/// predictor already handles the common case well. We still preload new_nh for
/// the next handler but dispatch through np->handler directly.
fn dispatch_guard_check() -> &'static str {
    if cfg!(target_os = "windows") {
        r#"    NextHandler new_nh = (NextHandler)pc_next(np)->handler;
    __attribute__((musttail)) return np->handler(ARGS_NEXT);"#
    } else {
        r#"    NextHandler new_nh = (NextHandler)pc_next(np)->handler;
    if (likely(np == pc_next(pc))) {
        __attribute__((musttail)) return ((OpHandler)nh)(ARGS_NEXT);
    } else {
        __attribute__((musttail)) return np->handler(ARGS_NEXT);
    }"#
    }
}

/// Generate the dispatch tail for nonlinear pattern.
/// Used for handlers that never return pc_next(pc).
fn dispatch_nonlinear() -> &'static str {
    r#"    (void)nh;
    NextHandler new_nh = (NextHandler)pc_next(np)->handler;
    __attribute__((musttail)) return np->handler(ARGS_NEXT);"#
}

/// Emit C wrappers for one handler name, dispatching on TOS pattern.
fn emit_wrappers_for_name(out: &mut String, name: &str, source: &dyn HandlerVariantSource) {
    let nonlinear = is_nonlinear(source.dispatch());
    let c_impl = source.c_impl();

    match source.tos_pattern() {
        Some(TosPattern::String(TosPatternString::All)) => {
            generate_all_tos_wrapper(out, name, c_impl, nonlinear);
        }
        Some(TosPattern::String(TosPatternString::None)) | None => {
            generate_single_wrapper(out, name, c_impl, nonlinear);
        }
        Some(TosPattern::PopPush { .. }) => {
            for variant in tos_variant_names() {
                generate_variant_wrapper_unified(out, name, c_impl, source.tos_pattern(), &variant, nonlinear);
            }
        }
    }
}

/// Generate C wrapper include file
pub fn generate(handlers: &HandlersFile, out_dir: &PathBuf) {
    let mut output = String::new();

    output.push_str("// Auto-generated by build.rs - DO NOT EDIT\n");
    output.push_str("// Fast interpreter C wrapper declarations\n");
    output.push_str("// Next-handler preloading: guard-check (default) and nonlinear dispatch\n\n");

    // Generate wrappers for regular handlers
    for handler in &handlers.handler {
        for expanded in handler.expand() {
            // Skip handlers with special handling in vm_trampoline.c
            if expanded.name == "term" || expanded.name == "trap" {
                continue;
            }
            emit_wrappers_for_name(&mut output, &expanded.name, handler);
        }
    }

    // Generate wrappers for fused handlers
    output.push_str("\n#ifdef FUSION_ENABLED\n");
    output.push_str("// Fused instruction handlers\n");
    for fused in &handlers.fused {
        emit_wrappers_for_name(&mut output, &fused.op, fused);
    }
    output.push_str("#endif // FUSION_ENABLED\n");

    let out_path = out_dir.join("fast_c_wrappers.inc");
    fs::write(&out_path, output).expect("Failed to write fast_c_wrappers.inc");
}

/// Generate a D1-D4 variant wrapper that passes operand pointers to impl
/// Unified generator used by both handler and fused handler code paths
fn generate_variant_wrapper_unified(out: &mut String, name: &str, c_impl: bool, tos_pattern: Option<&TosPattern>, variant: &str, nonlinear: bool) {
    let operand_params = get_operand_params(tos_pattern);
    let register_args = get_register_args(tos_pattern, variant);
    let dispatch = if nonlinear { dispatch_nonlinear() } else { dispatch_guard_check() };

    // For Rust-implemented handlers, declare the extern impl with operand params
    let impl_decl = if c_impl {
        String::new()
    } else {
        format!("extern struct Instruction* impl_{}(IMPL_PARAMS_BASE{});\n", name, operand_params)
    };

    out.push_str(&format!(
        r#"{impl_decl}PRESERVE_NONE
void op_{name}_{variant}(PARAMS) {{
    FAST_TRACE_HOOK({name}_{variant});
    FAST_PROFILE_HOOK({name});
    struct Instruction* np = impl_{name}(IMPL_ARGS_BASE, {register_args});
{dispatch}
}}

"#,
        impl_decl = impl_decl,
        name = name,
        variant = variant,
        register_args = register_args,
        dispatch = dispatch,
    ));
}

/// Generate a single wrapper (no variant suffix) for handlers with tos_pattern = "none"
fn generate_single_wrapper(out: &mut String, name: &str, c_impl: bool, nonlinear: bool) {
    let dispatch = if nonlinear { dispatch_nonlinear() } else { dispatch_guard_check() };

    // For Rust-implemented handlers, declare the extern impl (base params only)
    let impl_decl = if c_impl {
        String::new()
    } else {
        format!("extern struct Instruction* impl_{}(IMPL_PARAMS_BASE);\n", name)
    };

    out.push_str(&format!(
        r#"{impl_decl}PRESERVE_NONE
void op_{name}(PARAMS) {{
    FAST_TRACE_HOOK({name});
    FAST_PROFILE_HOOK({name});
    struct Instruction* np = impl_{name}(IMPL_ARGS_BASE);
{dispatch}
}}

"#,
        impl_decl = impl_decl,
        name = name,
        dispatch = dispatch,
    ));
}

/// Generate wrapper for handlers with tos_pattern = "all" (spill/fill)
/// These handlers need access to all TOS registers
fn generate_all_tos_wrapper(out: &mut String, name: &str, c_impl: bool, nonlinear: bool) {
    use super::tos_config::{tos_all_ptr_params, tos_all_register_args};

    let ptr_params = tos_all_ptr_params();
    let impl_args = tos_all_register_args();
    let dispatch = if nonlinear { dispatch_nonlinear() } else { dispatch_guard_check() };

    let impl_decl = if c_impl {
        String::new()
    } else {
        format!("extern struct Instruction* impl_{}(IMPL_PARAMS_BASE, {});\n", name, ptr_params)
    };

    out.push_str(&format!(
        r#"{impl_decl}PRESERVE_NONE
void op_{name}(PARAMS) {{
    FAST_TRACE_HOOK({name});
    FAST_PROFILE_HOOK({name});
    struct Instruction* np = impl_{name}(IMPL_ARGS_BASE, {impl_args});
{dispatch}
}}

"#,
        impl_decl = impl_decl,
        name = name,
        impl_args = impl_args,
        dispatch = dispatch,
    ));
}

/// Get operand parameter declarations for impl function signature
fn get_operand_params(tos_pattern: Option<&TosPattern>) -> &'static str {
    match tos_pattern {
        Some(TosPattern::PopPush { pop: 2, push: 1 }) => {
            // pop2_push1: lhs, rhs, dst
            ", uint64_t* p_lhs, uint64_t* p_rhs, uint64_t* p_dst"
        }
        Some(TosPattern::PopPush { pop: 1, push: 1 }) => {
            // pop1_push1: src, dst (same register, but pass both for consistency)
            ", uint64_t* p_src, uint64_t* p_dst"
        }
        Some(TosPattern::PopPush { pop: 0, push: 1 }) => {
            // pop0_push1: dst only
            ", uint64_t* p_dst"
        }
        Some(TosPattern::PopPush { pop: 2, push: 0 }) => {
            // pop2_push0: addr, val (for stores)
            ", uint64_t* p_addr, uint64_t* p_val"
        }
        Some(TosPattern::PopPush { pop: 1, push: 0 }) => {
            // pop1_push0: src only
            ", uint64_t* p_src"
        }
        Some(TosPattern::PopPush { pop: 3, push: 1 }) => {
            // pop3_push1: val1 (third), val2 (second), cond (top), dst
            ", uint64_t* p_val1, uint64_t* p_val2, uint64_t* p_cond, uint64_t* p_dst"
        }
        Some(TosPattern::PopPush { pop: 3, push: 0 }) => {
            // pop3_push0: a, b, c (for bulk ops)
            ", uint64_t* p_a, uint64_t* p_b, uint64_t* p_c"
        }
        Some(TosPattern::PopPush { pop: 1, push: 2 }) => {
            // pop1_push2: src, dst0, dst1 (tee_xor_get)
            ", uint64_t* p_src, uint64_t* p_dst0, uint64_t* p_dst1"
        }
        Some(TosPattern::PopPush { pop: 2, push: 2 }) => {
            // pop2_push2: lhs, rhs, dst0, dst1 (add_tee_get, add_set_get_const)
            ", uint64_t* p_lhs, uint64_t* p_rhs, uint64_t* p_dst0, uint64_t* p_dst1"
        }
        _ => "",
    }
}

/// Get register argument expressions for variant wrapper call to impl
fn get_register_args(tos_pattern: Option<&TosPattern>, variant: &str) -> String {
    use super::tos_config::get_register_args_for_pattern;

    // Parse variant index from "D1", "D2", etc.
    let variant_idx: usize = variant[1..].parse::<usize>().unwrap() - 1;

    match tos_pattern {
        Some(TosPattern::PopPush { pop, push }) => {
            get_register_args_for_pattern(*pop, *push, variant_idx)
        }
        _ => panic!("Unexpected tos_pattern for variant generation"),
    }
}
