// Rust extern declaration generation for fast interpreter
// Generates extern "C" fn declarations for handlers.rs
//
// Phase 3: Generate D1-D4 variant declarations for handlers with tos_pattern,
// single declarations for handlers with tos_pattern = "none".
// All declarations use 9-param signature (sp removed).

use std::fs;
use std::path::PathBuf;

use super::code_writer::CodeWriter;
use super::tos_config::tos_variant_names;
use super::types::{HandlersFile, HandlerVariantSource};

/// Emit extern declarations for a handler variant source
fn emit_decls(w: &mut CodeWriter, source: &dyn HandlerVariantSource) {
    let needs_variants = source.needs_variants();
    for name in source.expanded_names() {
        if needs_variants {
            for variant in tos_variant_names() {
                generate_handler_decl(w, &format!("{}_{}", name, variant));
            }
        } else {
            generate_handler_decl(w, &name);
        }
    }
}

/// Generate Rust extern declarations
pub fn generate(handlers: &HandlersFile, out_dir: &PathBuf) {
    let mut w = CodeWriter::new();

    w.line("// Auto-generated by build.rs - DO NOT EDIT");
    w.line("// Fast interpreter extern handler declarations");
    w.line("// Phase 3: 9-param signature (sp removed), D1-D4 variants for TOS handlers");
    w.blank();
    w.line("use crate::vm::interp::fast::{context::Context, instruction::Instruction};");
    w.blank();
    w.line("#[allow(improper_ctypes)]");
    w.line("extern \"C\" {");
    w.indent();

    // Generate declarations for regular handlers
    for handler in &handlers.handler {
        emit_decls(&mut w, handler);
    }

    w.dedent();
    w.line("}");

    // Generate declarations for fused handlers (gated by fusion feature)
    w.blank();
    w.line("#[cfg(feature = \"fusion\")]");
    w.line("#[allow(improper_ctypes)]");
    w.line("extern \"C\" {");
    w.indent();
    for fused in &handlers.fused {
        emit_decls(&mut w, fused);
    }
    w.dedent();
    w.line("}");

    let out_path = out_dir.join("fast_extern_decl.rs");
    fs::write(&out_path, w.finish()).expect("Failed to write fast_extern_decl.rs");
}

/// Generate a single handler declaration with nh parameter for next-handler preloading
fn generate_handler_decl(w: &mut CodeWriter, name: &str) {
    use super::tos_config::tos_register_names;

    wln!(w, "pub fn op_{}(", name);
    w.indent();
    w.line("ctx: *mut Context,");
    w.line("pc: *mut Instruction,");
    w.line("fp: *mut u64,");
    w.line("l0: u64,");
    for reg in tos_register_names() {
        wln!(w, "{}: u64,", reg);
    }
    w.line("nh: crate::vm::interp::fast::handlers::NextHandler,");
    w.dedent();
    w.line(");");
    w.blank();
}
