// Fusion pattern matching code generator.
// Produces fast_fusion.rs: FusedOp enum, OpFuser struct, try_match_N, try_fuse_*.

use super::op_classify::{get_pop_push, has_branch, has_branch_or_if, has_if, immediate_variant, is_any_local_op, is_comparison_op, is_l0_local_op, is_l1_local_op, is_l2_local_op, is_tos_none, pattern_op_to_opcode, CategoryMap};
use super::types::{bits_to_rust_type, to_pascal_case, FieldKind, FusedHandler};

/// Whether a try_fuse_* method needs the `stack` parameter.
/// Required for branch/if patterns (target resolution) and patterns with
/// local ops (l0 check and index remapping).
fn needs_stack_param(fused: &FusedHandler) -> bool {
    has_branch_or_if(fused) || fused.pattern.iter().any(|op| is_any_local_op(op))
}

/// Generate empty stub when no fused entries exist.
pub fn generate_empty() -> String {
    let mut code = String::new();
    code.push_str("// Auto-generated — no fused patterns\n\n");
    code.push_str("use crate::op_decoder::{Immediate, OpStream};\n");
    code.push_str("use crate::opcodes::WasmOpcode;\n\n");
    code.push_str("#[derive(Debug, Clone)]\n");
    code.push_str("pub enum FusedOp {\n");
    code.push_str("    Single { wasm_op: WasmOpcode, imm: Immediate },\n");
    code.push_str("}\n\n");
    code.push_str("pub struct OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    stream: &'s mut OpStream<'d, 'a, 'b>,\n");
    code.push_str("}\n\n");
    code.push_str("impl<'s, 'd, 'a, 'b> OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    pub fn new(stream: &'s mut OpStream<'d, 'a, 'b>) -> Self {\n");
    code.push_str("        Self { stream }\n");
    code.push_str("    }\n\n");
    code.push_str("    pub fn next(&mut self, _stack: &StackTracker) -> Result<Option<FusedOp>, crate::WasmError> {\n");
    code.push_str("        match self.stream.next()? {\n");
    code.push_str("            Some(decoded) => Ok(Some(FusedOp::Single {\n");
    code.push_str("                wasm_op: decoded.wasm_op,\n");
    code.push_str("                imm: decoded.imm.clone(),\n");
    code.push_str("            })),\n");
    code.push_str("            None => Ok(None),\n");
    code.push_str("        }\n");
    code.push_str("    }\n");
    code.push_str("}\n");
    code
}

/// Generate fast_fusion.rs content from fused handler definitions.
pub fn generate(fused_handlers: &[FusedHandler], categories: &CategoryMap) -> String {
    let mut code = String::new();

    code.push_str("// Auto-generated by gen_fusion.rs from handlers.toml\n");
    code.push_str("// DO NOT EDIT MANUALLY\n\n");
    // Note: this file is include!()'d directly into builder/mod.rs,
    // so imports must be relative to the builder module scope.
    code.push_str("use crate::op_decoder::{Immediate, OpStream};\n");
    code.push_str("use crate::opcodes::{Opcode, WasmOpcode};\n\n");

    // --- FusedOp enum ---
    code.push_str("/// Result of consuming instruction(s) from the stream.\n");
    code.push_str("#[derive(Debug, Clone)]\n");
    code.push_str("pub enum FusedOp {\n");
    code.push_str("    /// Single opcode (not fused)\n");
    code.push_str("    Single {\n");
    code.push_str("        wasm_op: WasmOpcode,\n");
    code.push_str("        imm: Immediate,\n");
    code.push_str("    },\n");

    for fused in fused_handlers {
        let variant = to_pascal_case(&fused.op);
        let fields = fused.get_fields();
        let is_if_pattern = has_if(fused);

        code.push_str(&format!("    /// Fused: {}\n", fused.pattern.join(" -> ")));
        code.push_str(&format!("    {} {{\n", variant));

        for field in fields {
            if field.kind == FieldKind::Target {
                if is_if_pattern {
                    // IF patterns: target is patched at ELSE/END, not stored in FusedOp
                    continue;
                }
                // br_if: Branch target stored as label index
                code.push_str("        target_label: u32,\n");
            } else {
                let rust_type = bits_to_rust_type(field.bits);
                code.push_str(&format!("        {}: {},\n", field.name, rust_type));
            }
        }

        code.push_str("    },\n");
    }

    code.push_str("}\n\n");

    // --- OpFuser struct ---
    code.push_str("/// Iterator adapter that performs fusion on the opcode stream.\n");
    code.push_str("pub struct OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    stream: &'s mut OpStream<'d, 'a, 'b>,\n");
    code.push_str("}\n\n");

    code.push_str("impl<'s, 'd, 'a, 'b> OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    pub fn new(stream: &'s mut OpStream<'d, 'a, 'b>) -> Self {\n");
    code.push_str("        Self { stream }\n");
    code.push_str("    }\n\n");

    // --- next() method ---
    // Group patterns by first opcode for dispatch
    let mut by_first_opcode: Vec<(String, Vec<&FusedHandler>)> = Vec::new();
    for fused in fused_handlers {
        let first_op = pattern_op_to_opcode(categories, &fused.pattern[0]);
        if let Some(entry) = by_first_opcode.iter_mut().find(|(op, _)| *op == first_op) {
            entry.1.push(fused);
        } else {
            by_first_opcode.push((first_op, vec![fused]));
        }
    }
    // Sort each group by pattern length (longest first)
    for (_, group) in &mut by_first_opcode {
        group.sort_by(|a, b| b.pattern.len().cmp(&a.pattern.len()));
    }
    // Sort groups alphabetically for deterministic output
    by_first_opcode.sort_by(|a, b| a.0.cmp(&b.0));

    code.push_str("    /// Get the next operation (fused or single).\n");
    code.push_str("    pub fn next(&mut self, stack: &StackTracker) -> Result<Option<FusedOp>, WasmError> {\n");
    code.push_str("        let first = match self.stream.peek()? {\n");
    code.push_str("            Some(op) => op.wasm_op,\n");
    code.push_str("            None => return Ok(None),\n");
    code.push_str("        };\n\n");
    code.push_str("        match first {\n");

    for (opcode, group) in &by_first_opcode {
        code.push_str(&format!(
            "            WasmOpcode::OP(Opcode::{}) => {{\n",
            opcode
        ));
        for fused in group {
            if needs_stack_param(fused) {
                code.push_str(&format!(
                    "                if let Some(fused) = self.try_fuse_{}(stack)? {{\n",
                    fused.op
                ));
            } else {
                code.push_str(&format!(
                    "                if let Some(fused) = self.try_fuse_{}()? {{\n",
                    fused.op
                ));
            }
            code.push_str("                    return Ok(Some(fused));\n");
            code.push_str("                }\n");
        }
        code.push_str("            }\n");
    }

    code.push_str("            _ => {}\n");
    code.push_str("        }\n\n");
    code.push_str("        // No fusion - return single\n");
    code.push_str("        let decoded = self.stream.next()?.unwrap();\n");
    code.push_str("        Ok(Some(FusedOp::Single {\n");
    code.push_str("            wasm_op: decoded.wasm_op,\n");
    code.push_str("            imm: decoded.imm.clone(),\n");
    code.push_str("        }))\n");
    code.push_str("    }\n\n");

    // --- try_match_N helpers ---
    // Generate for all pattern lengths that appear in the fused handlers
    let mut needed_sizes: Vec<usize> = fused_handlers.iter().map(|f| f.pattern.len()).collect();
    needed_sizes.sort();
    needed_sizes.dedup();
    for n in needed_sizes {
        generate_try_match_n(&mut code, n);
    }

    // --- Per-pattern try_fuse_* methods ---
    for fused in fused_handlers {
        generate_try_fuse(&mut code, fused, categories);
    }

    code.push_str("}\n"); // end impl OpFuser

    code
}

fn generate_try_match_n(code: &mut String, n: usize) {
    code.push_str(&format!(
        "    /// Try to match a {}-opcode pattern. Returns immediates if matched.\n",
        n
    ));
    code.push_str(&format!(
        "    fn try_match_{}(\n        &mut self,\n        ops: [Opcode; {}],\n    ) -> Result<Option<[Immediate; {}]>, WasmError> {{\n",
        n, n, n
    ));

    // Phase 1: check all opcodes without cloning immediates
    code.push_str("        for (i, expected_op) in ops.iter().enumerate() {\n");
    code.push_str("            match self.stream.peek_at(i)? {\n");
    code.push_str("                Some(op) if matches!(op.wasm_op, WasmOpcode::OP(o) if o == *expected_op) => {}\n");
    code.push_str("                _ => return Ok(None),\n");
    code.push_str("            }\n");
    code.push_str("        }\n\n");

    // Phase 2: clone immediates only on full opcode match (unrolled)
    code.push_str("        // All opcodes matched — clone immediates\n");
    code.push_str("        let imms = [\n");
    for i in 0..n {
        code.push_str(&format!(
            "            self.stream.peek_at({})?.unwrap().imm.clone(),\n",
            i
        ));
    }
    code.push_str("        ];\n\n");

    code.push_str(&format!(
        "        // Match! Return imms (caller must call stream.skip({}) to consume).\n",
        n
    ));
    code.push_str("        Ok(Some(imms))\n");
    code.push_str("    }\n\n");
}

fn generate_try_fuse(code: &mut String, fused: &FusedHandler, categories: &CategoryMap) {
    let variant = to_pascal_case(&fused.op);
    let n = fused.pattern.len();
    let fields = fused.get_fields();
    let is_br = has_branch(fused);
    let is_if = has_if(fused);
    let needs_stack = needs_stack_param(fused);

    code.push_str(&format!(
        "    /// Pattern: {}\n",
        fused.pattern.join(" -> ")
    ));
    if needs_stack {
        code.push_str(&format!(
            "    fn try_fuse_{}(&mut self, stack: &StackTracker) -> Result<Option<FusedOp>, WasmError> {{\n",
            fused.op
        ));
    } else {
        code.push_str(&format!(
            "    fn try_fuse_{}(&mut self) -> Result<Option<FusedOp>, WasmError> {{\n",
            fused.op
        ));
    }
    code.push_str("        use Opcode::*;\n");

    // Build the opcode array
    let opcodes: Vec<String> = fused
        .pattern
        .iter()
        .map(|op| pattern_op_to_opcode(categories, op).to_string())
        .collect();
    let imms_binding = "imms";
    code.push_str(&format!(
        "        let Some({}) = self.try_match_{}([{}])? else {{\n",
        imms_binding,
        n,
        opcodes.join(", ")
    ));
    code.push_str("            return Ok(None);\n");
    code.push_str("        };\n\n");

    // L0 position checks: for pattern positions that are l0 ops (local_get_l0 etc.),
    // verify the captured local index actually remaps to 0 (the hot local).
    for (i, op) in fused.pattern.iter().enumerate() {
        if is_l0_local_op(op) {
            let var = format!("_l0_raw_{}", i);
            code.push_str(&format!(
                "        let Immediate::LocalIndex({}) = imms[{}] else {{ return Ok(None) }};\n",
                var, i
            ));
            code.push_str(&format!(
                "        if !(stack.remap_local({}) == 0 && stack.has_l0()) {{ return Ok(None); }}\n",
                var
            ));
        }
        if is_l1_local_op(op) {
            let var = format!("_l1_raw_{}", i);
            code.push_str(&format!(
                "        let Immediate::LocalIndex({}) = imms[{}] else {{ return Ok(None) }};\n",
                var, i
            ));
            code.push_str(&format!(
                "        if !(stack.remap_local({}) == 1 && stack.has_l1()) {{ return Ok(None); }}\n",
                var
            ));
        }
        if is_l2_local_op(op) {
            let var = format!("_l2_raw_{}", i);
            code.push_str(&format!(
                "        let Immediate::LocalIndex({}) = imms[{}] else {{ return Ok(None) }};\n",
                var, i
            ));
            code.push_str(&format!(
                "        if !(stack.remap_local({}) == 2 && stack.has_hot_local(2)) {{ return Ok(None); }}\n",
                var
            ));
        }
    }

    // Extract immediates from the matched ops
    for field in fields {
        let from_idx = field
            .from
            .expect(&format!(
                "Field '{}' in fused handler '{}' missing 'from' annotation",
                field.name, fused.op
            ));
        let pattern_op = &fused.pattern[from_idx];
        let imm_variant = immediate_variant(categories, pattern_op);

        if field.kind == FieldKind::Target {
            if imm_variant == "LabelIndex" {
                // br_if: extract label index for branch resolution
                code.push_str(&format!(
                    "        let Immediate::LabelIndex(target_label) = imms[{}] else {{ return Ok(None) }};\n",
                    from_idx
                ));
            } else if imm_variant == "Block" {
                // if_: target field exists in encoding but label comes from patching,
                // not from the instruction immediate. We validate the block type below.
            } else {
                panic!("target field in '{}' references op '{}' with unexpected immediate variant '{}'",
                    fused.op, pattern_op, imm_variant);
            }
        } else {
            match imm_variant {
                "LocalIndex" => {
                    code.push_str(&format!(
                        "        let Immediate::LocalIndex({}) = imms[{}] else {{ return Ok(None) }};\n",
                        field.name, from_idx
                    ));
                    // L0 exclusion: non-l0 local patterns must not match the hot local.
                    // Remap the index (hot_local K ↔ 0) for correct fp[] addressing.
                    code.push_str(&format!(
                        "        if stack.has_l0() && stack.remap_local({}) == 0 {{ return Ok(None); }}\n",
                        field.name
                    ));
                    // L1 exclusion: non-l1 local patterns must not match the second hot local.
                    // Fused handlers read/write fp[idx] directly, bypassing the l1 register.
                    code.push_str(&format!(
                        "        if stack.has_l1() && stack.remap_local({}) == 1 {{ return Ok(None); }}\n",
                        field.name
                    ));
                    // L2 exclusion: non-l2 local patterns must not match the third hot local.
                    code.push_str(&format!(
                        "        if stack.has_hot_local(2) && stack.remap_local({}) == 2 {{ return Ok(None); }}\n",
                        field.name
                    ));
                    code.push_str(&format!(
                        "        let {} = stack.remap_local({});\n",
                        field.name, field.name
                    ));
                    // Guard: fused encoding uses 8-bit local indices; bail if >= 256
                    code.push_str(&format!(
                        "        if {} >= 256 {{ return Ok(None); }}\n",
                        field.name
                    ));
                }
                "I32" => {
                    code.push_str(&format!(
                        "        let Immediate::I32({}) = imms[{}] else {{ return Ok(None) }};\n",
                        field.name, from_idx
                    ));
                }
                "I64" => {
                    code.push_str(&format!(
                        "        let Immediate::I64({}) = imms[{}] else {{ return Ok(None) }};\n",
                        field.name, from_idx
                    ));
                }
                "MemArg" => {
                    // Fused handlers only support memory 0 (use ctx_mem0_base/size).
                    // Bail out for non-zero memidx so the Rust slow path handles them.
                    if field.name == "offset" {
                        code.push_str(&format!(
                            "        let Immediate::MemArg {{ offset, memidx, .. }} = imms[{}] else {{ return Ok(None) }};\n",
                            from_idx
                        ));
                    } else {
                        code.push_str(&format!(
                            "        let Immediate::MemArg {{ offset: {}, memidx, .. }} = imms[{}] else {{ return Ok(None) }};\n",
                            field.name, from_idx
                        ));
                    }
                    code.push_str("        if memidx != 0 { return Ok(None); }\n");
                }
                "None" => {
                    // The field doesn't come from an instruction immediate - skip
                    // This shouldn't happen since all fields have a from that points to ops with immediates
                    panic!("Field '{}' in '{}' references op '{}' which has no immediate",
                        field.name, fused.op, pattern_op);
                }
                _ => panic!("Unhandled immediate variant: {}", imm_variant),
            }
        }
    }

    // For if_ patterns: only fuse when block type is empty (no params, no results).
    // This avoids needing access to the CompileContext to resolve complex block types.
    if is_if {
        let if_idx = fused.pattern.iter().position(|op| op == "if_").unwrap();
        code.push_str(&format!(
            "        let Immediate::Block(ref block_type) = imms[{}] else {{ return Ok(None) }};\n",
            if_idx
        ));
        code.push_str("        if !matches!(block_type, crate::op_decoder::BlockType::Empty) {\n");
        code.push_str("            return Ok(None);\n");
        code.push_str("        }\n");
    }

    // For br_if patterns, check simplicity before consuming
    if is_br {
        let tos_none = is_tos_none(fused);
        let (pop, push) = get_pop_push(fused);
        let height_expr = if tos_none {
            // TOS None means (0, 0) net effect from the non-br_if ops
            "stack.height() + 1".to_string()
        } else {
            format!("stack.height() + {} - {} + 1", push, pop)
        };
        code.push_str(&format!(
            "        let height_at_brif = {};\n",
            height_expr
        ));
        code.push_str("        if !stack.is_br_if_simple_at(target_label, height_at_brif) {\n");
        code.push_str("            return Ok(None);\n");
        code.push_str("        }\n");
    }

    // Phase 1: Branch-aware pattern yielding.
    // For non-branch patterns ending with a comparison op: if the next instruction
    // is br_if or if_, yield (return None) so shorter patterns get a chance,
    // leaving the comparison available for branch fusion.
    if !is_br && !is_if {
        let last_op = fused.pattern.last().unwrap();
        if is_comparison_op(last_op) {
            code.push_str("        // Branch-aware yielding: comparison at end, check if next op is a branch\n");
            code.push_str(&format!(
                "        if let Some(next) = self.stream.peek_at({})? {{\n", n));
            code.push_str("            if matches!(next.wasm_op, WasmOpcode::OP(o) if o == BR_IF || o == IF) {\n");
            code.push_str("                return Ok(None);\n");
            code.push_str("            }\n");
            code.push_str("        }\n");
        }
    }

    // All checks passed — consume the matched instructions and build the variant
    code.push_str(&format!("\n        self.stream.skip({});\n", n));
    code.push_str(&format!("        Ok(Some(FusedOp::{} {{\n", variant));

    for field in fields {
        let from_idx = field.from.unwrap();
        let pattern_op = &fused.pattern[from_idx];
        let imm_variant = immediate_variant(categories, pattern_op);

        if field.kind == FieldKind::Target {
            if imm_variant == "LabelIndex" {
                // br_if: include target_label in the variant
                code.push_str("            target_label,\n");
            }
            // if_: target field is NOT included in FusedOp (patched later at ELSE/END)
        } else {
            let cast = match (imm_variant, field.bits) {
                ("LocalIndex", 8) => format!("{} as u8", field.name),
                ("LocalIndex", 16) => format!("{} as u16", field.name),
                ("I32", 32) => format!("{} as u32", field.name),
                ("I32", 8) => format!("{} as u8", field.name),
                ("I64", 64) => format!("{} as u64", field.name),
                ("I64", 32) => format!("{} as u32", field.name),
                ("MemArg", 32) => format!("{} as u32", field.name),
                _ => field.name.clone(),
            };
            code.push_str(&format!("            {}: {},\n", field.name, cast));
        }
    }

    code.push_str("        }))\n");
    code.push_str("    }\n\n");
}
