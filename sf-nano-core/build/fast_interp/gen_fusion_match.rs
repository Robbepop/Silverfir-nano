// Fusion pattern matching code generator.
// Produces fast_fusion.rs: FusedOp enum, OpFuser struct, try_match_N, try_fuse_*.

use super::op_classify::{immediate_variant, pattern_op_to_opcode, CategoryMap};
use super::types::{bits_to_rust_type, to_pascal_case, FieldKind, FusedHandler};

/// Generate empty stub when no fused entries exist.
pub fn generate_empty() -> String {
    let mut code = String::new();
    code.push_str("// Auto-generated — no fused patterns\n\n");
    code.push_str("use crate::op_decoder::{Immediate, OpStream};\n");
    code.push_str("use crate::opcodes::WasmOpcode;\n\n");
    code.push_str("#[derive(Debug, Clone)]\n");
    code.push_str("pub enum FusedOp {\n");
    code.push_str("    Single { wasm_op: WasmOpcode, imm: Immediate },\n");
    code.push_str("}\n\n");
    code.push_str("pub struct OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    stream: &'s mut OpStream<'d, 'a, 'b>,\n");
    code.push_str("}\n\n");
    code.push_str("impl<'s, 'd, 'a, 'b> OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    pub fn new(stream: &'s mut OpStream<'d, 'a, 'b>) -> Self {\n");
    code.push_str("        Self { stream }\n");
    code.push_str("    }\n\n");
    code.push_str("    pub fn next(&mut self) -> Result<Option<FusedOp>, crate::WasmError> {\n");
    code.push_str("        match self.stream.next()? {\n");
    code.push_str("            Some(decoded) => Ok(Some(FusedOp::Single {\n");
    code.push_str("                wasm_op: decoded.wasm_op,\n");
    code.push_str("                imm: decoded.imm.clone(),\n");
    code.push_str("            })),\n");
    code.push_str("            None => Ok(None),\n");
    code.push_str("        }\n");
    code.push_str("    }\n");
    code.push_str("}\n");
    code
}

/// Generate fast_fusion.rs content from fused handler definitions.
pub fn generate(fused_handlers: &[FusedHandler], categories: &CategoryMap) -> String {
    let mut code = String::new();

    code.push_str("// Auto-generated by gen_fusion.rs from handlers.toml\n");
    code.push_str("// DO NOT EDIT MANUALLY\n\n");
    // Note: this file is include!()'d directly into builder/mod.rs,
    // so imports must be relative to the builder module scope.
    code.push_str("use crate::op_decoder::{Immediate, OpStream};\n");
    code.push_str("use crate::opcodes::{Opcode, WasmOpcode};\n\n");

    // --- FusedOp enum ---
    code.push_str("/// Result of consuming instruction(s) from the stream.\n");
    code.push_str("#[derive(Debug, Clone)]\n");
    code.push_str("pub enum FusedOp {\n");
    code.push_str("    /// Single opcode (not fused)\n");
    code.push_str("    Single {\n");
    code.push_str("        wasm_op: WasmOpcode,\n");
    code.push_str("        imm: Immediate,\n");
    code.push_str("    },\n");

    for fused in fused_handlers {
        let variant = to_pascal_case(&fused.op);
        let fields = fused.get_fields();

        code.push_str(&format!("    /// Fused: {}\n", fused.pattern.join(" -> ")));
        code.push_str(&format!("    {} {{\n", variant));

        for field in fields {
            if field.kind == FieldKind::Target {
                // Branch target stored as label index
                code.push_str(&format!(
                    "        target_label: u32,\n"
                ));
            } else {
                let rust_type = bits_to_rust_type(field.bits);
                code.push_str(&format!("        {}: {},\n", field.name, rust_type));
            }
        }

        code.push_str("    },\n");
    }

    code.push_str("}\n\n");

    // --- OpFuser struct ---
    code.push_str("/// Iterator adapter that performs fusion on the opcode stream.\n");
    code.push_str("pub struct OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    stream: &'s mut OpStream<'d, 'a, 'b>,\n");
    code.push_str("}\n\n");

    code.push_str("impl<'s, 'd, 'a, 'b> OpFuser<'s, 'd, 'a, 'b> {\n");
    code.push_str("    pub fn new(stream: &'s mut OpStream<'d, 'a, 'b>) -> Self {\n");
    code.push_str("        Self { stream }\n");
    code.push_str("    }\n\n");

    // --- next() method ---
    code.push_str("    /// Get the next operation (fused or single).\n");
    code.push_str("    pub fn next(&mut self) -> Result<Option<FusedOp>, WasmError> {\n");
    code.push_str("        if self.stream.peek()?.is_none() {\n");
    code.push_str("            return Ok(None);\n");
    code.push_str("        }\n\n");

    // Group by pattern length and try longest first
    let mut by_length: Vec<(usize, Vec<&FusedHandler>)> = Vec::new();
    for fused in fused_handlers {
        let len = fused.pattern.len();
        if let Some(entry) = by_length.iter_mut().find(|(l, _)| *l == len) {
            entry.1.push(fused);
        } else {
            by_length.push((len, vec![fused]));
        }
    }
    by_length.sort_by(|a, b| b.0.cmp(&a.0)); // Longest first

    for (len, group) in &by_length {
        code.push_str(&format!("        // {}-way patterns\n", len));
        for fused in group {
            code.push_str(&format!(
                "        if let Some(fused) = self.try_fuse_{}()? {{\n",
                fused.op
            ));
            code.push_str("            return Ok(Some(fused));\n");
            code.push_str("        }\n");
        }
        code.push('\n');
    }

    code.push_str("        // No fusion - return single\n");
    code.push_str("        let decoded = self.stream.next()?.unwrap();\n");
    code.push_str("        Ok(Some(FusedOp::Single {\n");
    code.push_str("            wasm_op: decoded.wasm_op,\n");
    code.push_str("            imm: decoded.imm.clone(),\n");
    code.push_str("        }))\n");
    code.push_str("    }\n\n");

    // --- try_match_N helpers ---
    // Generate for all pattern lengths that appear in the fused handlers
    let mut needed_sizes: Vec<usize> = fused_handlers.iter().map(|f| f.pattern.len()).collect();
    needed_sizes.sort();
    needed_sizes.dedup();
    for n in needed_sizes {
        generate_try_match_n(&mut code, n);
    }

    // --- Per-pattern try_fuse_* methods ---
    for fused in fused_handlers {
        generate_try_fuse(&mut code, fused, categories);
    }

    code.push_str("}\n"); // end impl OpFuser

    code
}

fn generate_try_match_n(code: &mut String, n: usize) {
    code.push_str(&format!(
        "    /// Try to match a {}-opcode pattern. Returns immediates if matched.\n",
        n
    ));
    code.push_str(&format!(
        "    fn try_match_{}(\n        &mut self,\n        ops: [Opcode; {}],\n    ) -> Result<Option<[Immediate; {}]>, WasmError> {{\n",
        n, n, n
    ));

    // Initialize array
    code.push_str("        let mut imms = [\n");
    for _ in 0..n {
        code.push_str("            Immediate::None,\n");
    }
    code.push_str("        ];\n\n");

    code.push_str("        for (i, expected_op) in ops.iter().enumerate() {\n");
    code.push_str("            let (wasm_op, imm) = match self.stream.peek_at(i)? {\n");
    code.push_str("                Some(op) => (op.wasm_op, op.imm.clone()),\n");
    code.push_str("                None => return Ok(None),\n");
    code.push_str("            };\n");
    code.push_str(
        "            if !matches!(wasm_op, WasmOpcode::OP(o) if o == *expected_op) {\n",
    );
    code.push_str("                return Ok(None);\n");
    code.push_str("            }\n");
    code.push_str("            imms[i] = imm;\n");
    code.push_str("        }\n\n");

    code.push_str(&format!(
        "        // Match! Return imms (caller must call stream.skip({}) to consume).\n",
        n
    ));
    code.push_str("        Ok(Some(imms))\n");
    code.push_str("    }\n\n");
}

fn generate_try_fuse(code: &mut String, fused: &FusedHandler, categories: &CategoryMap) {
    let variant = to_pascal_case(&fused.op);
    let n = fused.pattern.len();
    let fields = fused.get_fields();

    code.push_str(&format!(
        "    /// Pattern: {}\n",
        fused.pattern.join(" -> ")
    ));
    code.push_str(&format!(
        "    fn try_fuse_{}(&mut self) -> Result<Option<FusedOp>, WasmError> {{\n",
        fused.op
    ));
    code.push_str("        use Opcode::*;\n");

    // Build the opcode array
    let opcodes: Vec<String> = fused
        .pattern
        .iter()
        .map(|op| pattern_op_to_opcode(categories, op).to_string())
        .collect();
    let imms_binding = if fields.is_empty() { "_imms" } else { "imms" };
    code.push_str(&format!(
        "        let Some({}) = self.try_match_{}([{}])? else {{\n",
        imms_binding,
        n,
        opcodes.join(", ")
    ));
    code.push_str("            return Ok(None);\n");
    code.push_str("        };\n\n");

    // Extract immediates from the matched ops
    for field in fields {
        let from_idx = field
            .from
            .expect(&format!(
                "Field '{}' in fused handler '{}' missing 'from' annotation",
                field.name, fused.op
            ));
        let pattern_op = &fused.pattern[from_idx];
        let imm_variant = immediate_variant(categories, pattern_op);

        if field.kind == FieldKind::Target {
            // Branch target
            assert_eq!(
                imm_variant, "LabelIndex",
                "target field must come from br_if"
            );
            code.push_str(&format!(
                "        let Immediate::LabelIndex(target_label) = imms[{}] else {{ return Ok(None) }};\n",
                from_idx
            ));
        } else {
            match imm_variant {
                "LocalIndex" => {
                    code.push_str(&format!(
                        "        let Immediate::LocalIndex({}) = imms[{}] else {{ return Ok(None) }};\n",
                        field.name, from_idx
                    ));
                }
                "I32" => {
                    code.push_str(&format!(
                        "        let Immediate::I32({}) = imms[{}] else {{ return Ok(None) }};\n",
                        field.name, from_idx
                    ));
                }
                "I64" => {
                    code.push_str(&format!(
                        "        let Immediate::I64({}) = imms[{}] else {{ return Ok(None) }};\n",
                        field.name, from_idx
                    ));
                }
                "MemArg" => {
                    // Fused handlers only support memory 0 (use ctx_mem0_base/size).
                    // Bail out for non-zero memidx so the Rust slow path handles them.
                    if field.name == "offset" {
                        code.push_str(&format!(
                            "        let Immediate::MemArg {{ offset, memidx, .. }} = imms[{}] else {{ return Ok(None) }};\n",
                            from_idx
                        ));
                    } else {
                        code.push_str(&format!(
                            "        let Immediate::MemArg {{ offset: {}, memidx, .. }} = imms[{}] else {{ return Ok(None) }};\n",
                            field.name, from_idx
                        ));
                    }
                    code.push_str("        if memidx != 0 { return Ok(None); }\n");
                }
                "None" => {
                    // The field doesn't come from an instruction immediate - skip
                    // This shouldn't happen since all fields have a from that points to ops with immediates
                    panic!("Field '{}' in '{}' references op '{}' which has no immediate",
                        field.name, fused.op, pattern_op);
                }
                _ => panic!("Unhandled immediate variant: {}", imm_variant),
            }
        }
    }

    // All checks passed — consume the matched instructions and build the variant
    code.push_str(&format!("\n        self.stream.skip({});\n", n));
    code.push_str(&format!("        Ok(Some(FusedOp::{} {{\n", variant));

    for field in fields {
        let from_idx = field.from.unwrap();
        let pattern_op = &fused.pattern[from_idx];
        let imm_variant = immediate_variant(categories, pattern_op);

        if field.kind == FieldKind::Target {
            code.push_str("            target_label,\n");
        } else {
            let cast = match (imm_variant, field.bits) {
                ("LocalIndex", 16) => format!("{} as u16", field.name),
                ("I32", 32) => format!("{} as u32", field.name),
                ("I32", 8) => format!("{} as u8", field.name),
                ("I64", 64) => format!("{} as u64", field.name),
                ("I64", 32) => format!("{} as u32", field.name),
                ("MemArg", 32) => format!("{} as u32", field.name),
                _ => field.name.clone(),
            };
            code.push_str(&format!("            {}: {},\n", field.name, cast));
        }
    }

    code.push_str("        }))\n");
    code.push_str("    }\n\n");
}
