//! Generate handler variant lookup tables.
//! Produces static arrays for each handler that has TOS variants.

use std::fs;
use std::path::PathBuf;

use super::code_writer::CodeWriter;
use super::tos_config::{tos_variant_names, TOS_REGISTER_COUNT};
use super::types::{HandlersFile, HandlerVariantSource};

pub fn generate(handlers: &HandlersFile, out_dir: &PathBuf) {
    let mut w = CodeWriter::new();

    w.line("// Auto-generated by build.rs - DO NOT EDIT");
    w.line("// Handler variant lookup tables for TOS register dispatch");
    w.blank();
    w.line("use crate::vm::interp::fast::handlers::full_set::*;");
    w.line("use crate::vm::interp::fast::handlers::OpHandler;");
    w.blank();

    // Generate const for documentation
    wln!(w, "/// Number of TOS register variants per handler");
    wln!(w, "pub const TOS_VARIANT_COUNT: usize = {};", TOS_REGISTER_COUNT);
    w.blank();

    // Generate type alias for handler arrays
    w.line("/// Handler variant array type");
    w.line("pub type HandlerVariants = [OpHandler; TOS_VARIANT_COUNT];");
    w.blank();

    let variants = tos_variant_names();

    // Generate arrays for regular handlers with TOS variants
    for handler in &handlers.handler {
        if handler.needs_variants() {
            for name in handler.expanded_names() {
                generate_handler_array(&mut w, &name, &variants);
            }
        }
    }

    // Generate arrays for fused handlers with TOS variants
    w.line("#[cfg(feature = \"fusion\")]");
    w.line("mod fused_lookup {");
    w.indent();
    w.line("use super::*;");
    for fused in &handlers.fused {
        if fused.needs_variants() {
            generate_handler_array(&mut w, &fused.op, &variants);
        }
    }
    w.dedent();
    w.line("}");
    w.line("#[cfg(feature = \"fusion\")]");
    w.line("pub use fused_lookup::*;");

    let out_path = out_dir.join("fast_handler_lookup.rs");
    fs::write(&out_path, w.finish()).expect("Failed to write fast_handler_lookup.rs");
}

fn generate_handler_array(w: &mut CodeWriter, name: &str, variants: &[String]) {
    // Convert handler name to uppercase for static name (e.g., i32_add -> I32_ADD)
    let static_name = name.to_uppercase();

    // Build variant list: [op_i32_add_D1, op_i32_add_D2, ...]
    let variant_list: Vec<String> = variants
        .iter()
        .map(|v| format!("op_{}_{}", name, v))
        .collect();

    wln!(w, "pub static {}: HandlerVariants = [{}];", static_name, variant_list.join(", "));
}
