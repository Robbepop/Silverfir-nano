// Handler name lookup table generation for fast interpreter profiler.
// Generates a mapping from handler function pointer to handler name string.
//
// Registers all D1-D4 variants for handlers with tos_pattern.

use std::fs;
use std::path::PathBuf;

use super::code_writer::CodeWriter;
use super::tos_config::TOS_REGISTER_COUNT;
use super::types::HandlersFile;

/// Generate handler names lookup table for profiler.
pub fn generate(handlers: &HandlersFile, out_dir: &PathBuf) {
    let mut w = CodeWriter::new();

    w.line("// Auto-generated by build.rs - DO NOT EDIT");
    w.line("// Handler name lookup table for fast interpreter profiler");
    w.line("// Registers all D1-D4 variants for handlers with tos_pattern");
    w.blank();

    // Collect all handlers with their variant info
    // For handlers with tos_pattern != "none", we register all D1-D4 variants
    let mut handler_entries: Vec<(String, String)> = Vec::new();

    for source in handlers.all_variant_sources() {
        let needs_variant = source.needs_variants();
        for name in source.expanded_names() {
            if needs_variant {
                // Register all D1-D4 variants with the same display name
                for d in 1..=TOS_REGISTER_COUNT {
                    let func_name = format!("op_{}_D{}", name, d);
                    handler_entries.push((func_name, name.clone()));
                }
            } else {
                let func_name = format!("op_{}", name);
                handler_entries.push((func_name, name));
            }
        }
    }

    // Generate the initializer function using the existing full_set module
    w.line("/// Initialize handler name lookup table.");
    w.line("/// Maps handler function pointers to their string names.");
    w.line("fn init_handler_names() -> HashMap<usize, &'static str> {");
    w.indent();
    w.line("use super::handlers::full_set::*;");
    wln!(w, "let mut map = HashMap::with_capacity({});", handler_entries.len());

    for (func_name, display_name) in &handler_entries {
        wln!(w, "map.insert({} as usize, \"{}\");", func_name, display_name);
    }

    w.line("map");
    w.dedent();
    w.line("}");

    let out_path = out_dir.join("handler_names.rs");
    fs::write(&out_path, w.finish()).expect("Failed to write handler_names.rs");
}
