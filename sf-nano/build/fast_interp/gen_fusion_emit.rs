// Fusion emission code generator.
// Produces fast_fusion_emit.rs: emit_fused dispatch, spill/fill helpers,
// CodeEmitter emit_fused_* methods.

use super::op_classify::{first_wasm_opcode, get_pop_push, has_branch, is_tos_none, pattern_op_to_opcode, to_upper_snake, CategoryMap};
use super::types::{bits_to_rust_type, to_pascal_case, FieldKind, FusedHandler};

/// Generate empty stub when no fused entries exist.
pub fn generate_empty() -> String {
    let mut code = String::new();
    code.push_str("// Auto-generated — no fused patterns\n\n");
    code.push_str("#[allow(unused)]\n");
    code.push_str("fn emit_fused(_fused: FusedOp, _stack: &mut StackTracker, _emitter: &mut CodeEmitter) {\n");
    code.push_str("    unreachable!(\"no fused patterns configured\")\n");
    code.push_str("}\n");
    code
}

/// Generate fast_fusion_emit.rs content from fused handler definitions.
pub fn generate(fused_handlers: &[FusedHandler], categories: &CategoryMap) -> String {
    let mut code = String::new();

    code.push_str("// Auto-generated by gen_fusion.rs from handlers.toml\n");
    code.push_str("// DO NOT EDIT MANUALLY\n\n");
    // Note: this file is include!()'d directly into builder/mod.rs,
    // so types like CodeEmitter, StackTracker, TempInst, FusedOp are already in scope.
    code.push_str("use super::handler_lookup;\n");
    code.push_str("use super::TOS_REGISTER_COUNT;\n");

    // Collect all Opcode constants used by first ops
    let mut first_opcodes: Vec<String> = Vec::new();
    for fused in fused_handlers {
        let oc = pattern_op_to_opcode(categories, &fused.pattern[0]);
        if !first_opcodes.contains(&oc.to_string()) {
            first_opcodes.push(oc.to_string());
        }
    }
    code.push_str(&format!(
        "use crate::opcodes::Opcode::{{\n    {},\n}};\n",
        first_opcodes.join(", ")
    ));

    code.push_str("use crate::vm::interp::fast::encoding::PatternData;\n");
    code.push_str("use crate::vm::interp::fast::handlers::full_set;\n");
    code.push_str("use crate::vm::interp::fast::handlers::OpHandler;\n\n");

    // --- Spill/Fill helpers ---
    code.push_str("// =============================================================================\n");
    code.push_str("// Spill and Fill Helpers\n");
    code.push_str("// =============================================================================\n\n");

    code.push_str("#[inline]\n");
    code.push_str("fn emit_spills_for_net_push(stack: &mut StackTracker, emitter: &mut CodeEmitter, net_push: usize) {\n");
    code.push_str("    if stack.is_unreachable() || net_push == 0 {\n");
    code.push_str("        return;\n");
    code.push_str("    }\n");
    code.push_str("    let tos_count = stack.tos_count();\n");
    code.push_str("    let spills_needed = (tos_count + net_push).saturating_sub(TOS_REGISTER_COUNT);\n");
    code.push_str("    if spills_needed > 0 {\n");
    code.push_str("        let spill_depth = stack.spill_depth();\n");
    code.push_str("        let slot = (stack.operand_base() + spill_depth + spills_needed - 1) as u16;\n");
    code.push_str("        let variant = ((spill_depth + spills_needed - 1) % TOS_REGISTER_COUNT) as u8;\n");
    code.push_str("        stack.record_spill(spills_needed);\n");
    code.push_str("        emitter.emit_spill(slot, spills_needed as u8, variant);\n");
    code.push_str("    }\n");
    code.push_str("}\n\n");

    code.push_str("#[inline]\n");
    code.push_str("fn emit_fill_for_operands(stack: &mut StackTracker, emitter: &mut CodeEmitter, operand_count: usize) {\n");
    code.push_str("    if stack.is_unreachable() {\n");
    code.push_str("        return;\n");
    code.push_str("    }\n");
    code.push_str("    let spill_depth = stack.spill_depth();\n");
    code.push_str("    let height = stack.height();\n");
    code.push_str("    let min_spill_for_operands = height.saturating_sub(operand_count);\n");
    code.push_str("    if spill_depth > min_spill_for_operands {\n");
    code.push_str("        let fill_count = spill_depth - min_spill_for_operands;\n");
    code.push_str("        let slot = (stack.operand_base() + spill_depth - 1) as u16;\n");
    code.push_str("        let variant = ((spill_depth - 1) % TOS_REGISTER_COUNT) as u8;\n");
    code.push_str("        stack.record_fill(fill_count);\n");
    code.push_str("        emitter.emit_fill(slot, fill_count as u8, variant);\n");
    code.push_str("    }\n");
    code.push_str("}\n\n");

    code.push_str("#[inline]\n");
    code.push_str("fn variant_index(output_depth: usize) -> usize {\n");
    code.push_str("    (output_depth + TOS_REGISTER_COUNT - 1) % TOS_REGISTER_COUNT\n");
    code.push_str("}\n\n");

    // --- emit_fused() dispatch ---
    code.push_str("// =============================================================================\n");
    code.push_str("// Main Fused Emission Entry Point\n");
    code.push_str("// =============================================================================\n\n");

    code.push_str("pub fn emit_fused(op: FusedOp, stack: &mut StackTracker, emitter: &mut CodeEmitter) {\n");
    code.push_str("    match op {\n");
    code.push_str("        FusedOp::Single { .. } => {\n");
    code.push_str("            unreachable!(\"emit_fused should not be called with Single\")\n");
    code.push_str("        }\n");

    for fused in fused_handlers {
        generate_emit_match_arm(&mut code, fused);
    }

    code.push_str("    }\n");
    code.push_str("}\n\n");

    // --- CodeEmitter emit_fused_* methods ---
    code.push_str("// =============================================================================\n");
    code.push_str("// CodeEmitter Fused Instruction Methods\n");
    code.push_str("// =============================================================================\n\n");
    code.push_str("impl CodeEmitter {\n");

    for fused in fused_handlers {
        generate_emit_method(&mut code, fused, categories);
    }

    code.push_str("}\n");

    code
}

fn generate_emit_match_arm(code: &mut String, fused: &FusedHandler) {
    let variant = to_pascal_case(&fused.op);
    let fields = fused.get_fields();
    let (pop, push) = get_pop_push(fused);
    let tos_none = is_tos_none(fused);
    let has_br = has_branch(fused);

    // Build the pattern destructuring
    let field_names: Vec<String> = fields
        .iter()
        .map(|f| {
            if f.kind == FieldKind::Target {
                "target_label".to_string()
            } else {
                f.name.clone()
            }
        })
        .collect();

    code.push_str(&format!(
        "        FusedOp::{} {{ {} }} => {{\n",
        variant,
        field_names.join(", ")
    ));

    if tos_none {
        // tos_pattern = "none" — no spill/fill, use full_set directly
        let emitter_args: Vec<String> = fields
            .iter()
            .filter(|f| f.kind != FieldKind::Target)
            .map(|f| f.name.clone())
            .collect();

        // Emit instruction — for "none" patterns, the emitter method doesn't take a handler
        code.push_str(&format!(
            "            let idx = emitter.emit_fused_{}({});\n",
            fused.op,
            emitter_args.join(", ")
        ));

        // Handle branch target
        if has_br {
            code.push_str("            let (_, target) = stack.branch_info(target_label);\n");
            code.push_str("            if let Some(tgt) = target {\n");
            code.push_str("                emitter.patch_alt(idx, tgt);\n");
            code.push_str("            } else {\n");
            code.push_str("                stack.register_forward_branch(target_label, idx, None);\n");
            code.push_str("            }\n");
        } else {
            // idx is unused
            code.push_str("            let _ = idx;\n");
        }
    } else {
        let net = push as i8 - pop as i8;

        // Determine spill/fill strategy
        if pop > 0 {
            code.push_str(&format!(
                "            emit_fill_for_operands(stack, emitter, {});\n",
                pop
            ));
        }
        if push > pop {
            // Net push — need spill for extra pushes
            // But if pop > 0, we might need fill first THEN spill
            // The original code does fill then spill in some cases (tee_xor_get)
            let net_push = (push - pop) as usize;
            code.push_str(&format!(
                "            emit_spills_for_net_push(stack, emitter, {});\n",
                net_push
            ));
        }

        // Compute output_depth for variant selection
        // output_depth = new stack height = height + net
        // This determines which TOS register position the topmost output lands in.
        if net > 0 {
            code.push_str(&format!(
                "            let output_depth = stack.height() + {};\n",
                net
            ));
        } else {
            code.push_str("            let output_depth = stack.height();\n");
        }

        let lookup_name = to_upper_snake(&fused.op);
        code.push_str(&format!(
            "            let handler = handler_lookup::{}[variant_index(output_depth)];\n",
            lookup_name
        ));

        // Build emitter call args
        let mut emitter_args = vec!["handler".to_string()];
        for f in fields {
            if f.kind == FieldKind::Target {
                // Skip target — handled by branch patching
                continue;
            }
            emitter_args.push(f.name.clone());
        }

        code.push_str(&format!(
            "            let idx = emitter.emit_fused_{}({});\n",
            fused.op,
            emitter_args.join(", ")
        ));

        // Stack effects
        if net < 0 {
            for _ in 0..(-net) {
                code.push_str("            stack.pop();\n");
            }
        } else if net > 0 {
            for _ in 0..net {
                code.push_str("            stack.push();\n");
            }
        }

        // Handle branch target
        if has_br {
            code.push_str("\n            let (_, target) = stack.branch_info(target_label);\n");
            code.push_str("            if let Some(tgt) = target {\n");
            code.push_str("                emitter.patch_alt(idx, tgt);\n");
            code.push_str("            } else {\n");
            code.push_str("                stack.register_forward_branch(target_label, idx, None);\n");
            code.push_str("            }\n");
        } else {
            code.push_str("            let _ = idx;\n");
        }
    }

    code.push_str("        }\n");
}

fn generate_emit_method(code: &mut String, fused: &FusedHandler, categories: &CategoryMap) {
    let variant = to_pascal_case(&fused.op);
    let fields = fused.get_fields();
    let tos_none = is_tos_none(fused);
    let first_op = first_wasm_opcode(categories, fused);

    // Build parameter list
    let mut params = Vec::new();
    if !tos_none {
        params.push("handler: OpHandler".to_string());
    }
    for f in fields {
        if f.kind == FieldKind::Target {
            // Target fields are not passed — they use TempInst.alt_idx
            continue;
        }
        let rust_type = bits_to_rust_type(f.bits);
        params.push(format!("{}: {}", f.name, rust_type));
    }

    code.push_str(&format!(
        "    pub fn emit_fused_{}(&mut self, {}) -> usize {{\n",
        fused.op,
        params.join(", ")
    ));

    // Build PatternData fields
    let pattern_data_fields: Vec<String> = fields
        .iter()
        .filter(|f| f.kind != FieldKind::Target)
        .map(|f| f.name.clone())
        .collect();

    let handler_expr = if tos_none {
        format!("full_set::op_{}", fused.op)
    } else {
        "handler".to_string()
    };

    let target_suffix = if has_branch(fused) { ".with_target()" } else { "" };

    if pattern_data_fields.is_empty() {
        code.push_str(&format!(
            "        self.emit(TempInst::new(\n            {},\n            PatternData::{},\n            {},\n        ){})\n",
            handler_expr, variant, first_op, target_suffix
        ));
    } else {
        code.push_str(&format!(
            "        self.emit(TempInst::new(\n            {},\n            PatternData::{} {{\n",
            handler_expr, variant
        ));
        for f in &pattern_data_fields {
            code.push_str(&format!("                {},\n", f));
        }
        code.push_str(&format!(
            "            }},\n            {},\n        ){})\n",
            first_op, target_suffix
        ));
    }

    code.push_str("    }\n\n");
}
