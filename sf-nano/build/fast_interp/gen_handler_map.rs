// Handler mapping generation for fast interpreter
// Generates map_handler() function body for ir_builder.rs
//
// Phase 1: Generates D1 variant for handlers with tos_pattern != "none",
// single handler for handlers with tos_pattern = "none".

use std::fs;
use std::path::PathBuf;

use super::types::{ExpandedHandler, HandlersFile, HandlerDef, TosPattern};

/// Generate handler mapping function body
/// This is included directly inside the map_handler() function in ir_builder.rs
pub fn generate(handlers: &HandlersFile, out_dir: &PathBuf) {
    let mut output = String::new();

    output.push_str("// Auto-generated by build.rs - DO NOT EDIT\n");
    output.push_str("// Fast interpreter handler mapping (function body)\n");
    output.push_str("// Phase 1: Uses D1 variant for handlers with tos_pattern\n");
    output.push_str("{\n");
    output.push_str("    use crate::opcodes::{Opcode::*, OpcodeFC::*, OpcodeFB::*, WasmOpcode::*};\n");
    output.push_str("    use crate::vm::interp::fast::handlers::full_set::*;\n");
    output.push_str("    let handler = match op {\n");

    // Generate match arms for all handlers
    for handler_def in &handlers.handler {
        for expanded in handler_def.expand() {
            if let Some(opcode_arm) = get_opcode_arm(&expanded) {
                let handler_name = get_handler_name(&expanded.name, handler_def);
                output.push_str(&format!(
                    "        {} => {},\n",
                    opcode_arm,
                    handler_name
                ));
            }
        }
    }

    // Default arm
    output.push_str("        _ => {\n");
    output.push_str("            return Err(WasmError::invalid(format!(\n");
    output.push_str("                \"Unsupported opcode for fast backend: {:?}\",\n");
    output.push_str("                op\n");
    output.push_str("            )));\n");
    output.push_str("        }\n");
    output.push_str("    };\n");
    output.push_str("    Ok(handler)\n");
    output.push_str("}\n");

    let out_path = out_dir.join("fast_handler_map.rs");
    fs::write(&out_path, output).expect("Failed to write fast_handler_map.rs");
}

/// Get the handler function name, with D1 suffix if needed
fn get_handler_name(base_name: &str, handler_def: &HandlerDef) -> String {
    // Only PopPush patterns need D1-D4 variants
    let needs_variant = matches!(
        handler_def.tos_pattern.as_ref(),
        Some(TosPattern::PopPush { .. })
    );

    if needs_variant {
        format!("op_{}_D1", base_name)
    } else {
        format!("op_{}", base_name)
    }
}

/// Map expanded handler to opcode match arm
fn get_opcode_arm(handler: &ExpandedHandler) -> Option<String> {
    let op = &handler.name;
    let prefix = handler.opcode_prefix.as_str();

    // Terminal and special handlers are not mapped from opcodes
    // These are internal handlers used by the IR builder, not WebAssembly opcodes
    if op == "term" || op == "trap" || op == "copy" || op == "preserve_copy" {
        return None;
    }

    // If handler has explicit wasm_op, use it
    if let Some(ref wasm_op) = handler.wasm_op {
        return Some(format!("{}({})", prefix, wasm_op));
    }

    // Handle special naming conventions
    let normalized = match op.as_str() {
        "if_" => "IF",
        "else_" => "ELSE",
        "loop" => "LOOP",
        "block" => "BLOCK",
        "end" => "END",
        "return" => "RETURN",
        "unreachable" => "UNREACHABLE",
        "nop" => "NOP",
        "drop" => "DROP",
        "select" => "SELECT",
        "select_t" => "SELECT_T",
        "br" => "BR",
        "br_if" => "BR_IF",
        "br_table" => "BR_TABLE",
        "call" => "CALL",
        "call_indirect" => "CALL_INDIRECT",
        "call_ref" => "CALL_REF",
        "global_get" => "GLOBAL_GET",
        "global_set" => "GLOBAL_SET",
        "ref_null" => "REF_NULL",
        "ref_is_null" => "REF_IS_NULL",
        "ref_func" => "REF_FUNC",
        "ref_as_non_null" => "REF_AS_NON_NULL",
        "ref_eq" => "REF_EQ",
        _ => {
            // Convert snake_case to UPPER_SNAKE_CASE
            return Some(format!("{}({})", prefix, op.to_uppercase()));
        }
    };

    Some(format!("{}({})", prefix, normalized))
}
