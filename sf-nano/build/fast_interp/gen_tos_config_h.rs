//! Generate tos_config.h for C handlers.

use std::fs;
use std::path::PathBuf;

use super::tos_config::{tos_register_names, TOS_REGISTER_COUNT};

pub fn generate(out_dir: &PathBuf) {
    let mut output = String::new();

    output.push_str("// Auto-generated by build.rs - DO NOT EDIT\n");
    output.push_str("// TOS register configuration for C handlers\n\n");
    output.push_str("#pragma once\n\n");

    output.push_str(&format!(
        "#define TOS_REGISTER_COUNT {}\n",
        TOS_REGISTER_COUNT
    ));
    output.push_str(&format!(
        "#define TOS_REGISTER_MASK {}\n",
        TOS_REGISTER_COUNT - 1
    ));
    output.push_str("\n");

    // Generate register names for reference
    let reg_names = tos_register_names();
    output.push_str("// TOS register names\n");
    for (i, name) in reg_names.iter().enumerate() {
        output.push_str(&format!("#define TOS_REG_{} {}\n", i, name));
    }
    output.push_str("\n");

    let out_path = out_dir.join("tos_config.h");
    fs::write(&out_path, output).expect("Failed to write tos_config.h");
}
