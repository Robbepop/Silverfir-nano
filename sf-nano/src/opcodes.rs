use alloc::format;
use crate::error::WasmError;
use core::fmt;

macro_rules! define_opcodes {
    ($mod_name:ident, $enum_name:ident, $type:ty, $(($name:ident, $display_name:expr, $value:expr)),* $(,)?) => {
        #[allow(non_snake_case, dead_code)]
        pub mod $mod_name {
            $(pub const $name: $type = $value;)*
        }

        #[derive(Copy, Clone, Debug, PartialEq)]
        #[allow(non_camel_case_types)]
        #[repr($type)]
        pub enum $enum_name {
            $($name = $value,)*
        }

        impl TryFrom<$type> for $enum_name {
            type Error = WasmError;

            fn try_from(value: $type) -> Result<Self, Self::Error> {
                match value {
                    $($value => Ok($enum_name::$name),)*
                    _ => Err(WasmError::malformed(format!("invalid opcode: {:#x}", value))),
                }
            }
        }

        impl fmt::Display for $enum_name {
            fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
                match *self {
                    $($enum_name::$name => write!(f, "{}", $display_name),)*
                }
            }
        }
    }
}

